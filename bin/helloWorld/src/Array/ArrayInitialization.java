
package Array;


public class ArrayInitialization {
    
    public static void main(String[] args) {
        
        
        int []a = new int[5];
        
        System.out.println(a[1]);
      
      int [][]b = new int[5][2];
        
       System.out.println(b[0][1]);
       
       double [][]c = new double[5][2];
        
       System.out.println(c[0][1]);
        
    }
    
}
